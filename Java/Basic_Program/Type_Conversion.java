package Basic_Program;

public class Type_Conversion {
    public static void main(String[] args){

        // Type Casting
        int a = 257;
        byte k = (byte) a;

        float f = 5.6f;
        int t = (int) f;

        // Type Promotion
        byte x = 10;
        byte y = 30;

        int result = x * y;
        System.out.println(result);
    }
    
}
